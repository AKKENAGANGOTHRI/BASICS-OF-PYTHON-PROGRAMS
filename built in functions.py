#examples for functions in python
num=[11,88,66,44,55,77]
#len()----------->returns the number of elements in the list
print("length of the list is:",len(num))
#max()----------->returns the maximum number of elements in the list
print("maximum number is:",max(num))
#len()----------->returns the minimum number of elements in the list
print("minimum number  is:",min(num))
#len()----------->returns the summation  of elements in the list
print("summation  is:",sum(num))
#len()----------->returns the sorted list
print("sorted list is:",sorted(num))
print(any(num))
print(all(num))